package Unit_5;

public class ManyLoops{

   public static void main(String [] args){
   
   for(int i = 10 ; i>=1 ; i--){
       System.out.print(i + "\t");
       }
       
   System.out.println("");
       
   for(int j = 25 ; j<=30 ; j++){
       System.out.print(j + "\t");
       }
   
   System.out.println("");
       
   for(int k = 0 ; k>= -20 ; k--){
       System.out.print(k + "\t");
       }
       

   }
}